def is_file_binary_in_git_diff_check(ref1, ref2, file_path):
    """
    Checks if Git considers a file binary when diffing between ref1 and ref2 for file_path.
    Uses `git diff --numstat`.
    Returns True if binary, False if text, None if error or ambiguous.
    """
    numstat_output, _, retcode = run_git_command(
        ['diff', '--numstat', ref1, ref2, '--', file_path],
        ignore_errors=True
    )
    if retcode == 0 and numstat_output:
        # numstat can output multiple lines if the file_path is a directory or wildcard.
        # We are interested in the line specific to the file_path.
        for line in numstat_output.strip().splitlines():
            parts = line.strip().split('\t')
            # For a specific file, output is usually: <added> <deleted> <filepath>
            # For binary files, <added> and <deleted> are '-'
            if len(parts) == 3 and parts[2] == file_path:
                if parts[0] == '-' and parts[1] == '-':
                    return True  # Explicitly binary
                else:
                    return False # Explicitly text
            elif len(parts) == 1 and parts[0] == file_path: # e.g. if file was just added
                # This specific case is harder to judge with numstat alone.
                # `git show` would be needed to truly know.
                # For diffing purposes, if it's just added, it might not show as binary here.
                # We might need a secondary check if this path is problematic.
                return False # Assume text for now if only path is shown by numstat
    return None # Ambiguous or error

def run_git_command(command, working_dir=None, expect_to_fail=False, ignore_errors=False):
    """
    Executes a Git command using subprocess.run and returns its output.
    Provides basic error handling and options to ignore certain errors.
    Args:
        command (list): The Git command and arguments (e.g., ['log', '-1']).
        working_dir (str, optional): Directory to run the command in. Defaults to current dir.
        expect_to_fail (bool, optional): If True, always returns (stdout, stderr, retcode) tuple.
        ignore_errors (bool, optional): If True, returns tuple even on non-zero exit code (but not FileNotFoundError).
    Returns:
        str or tuple or None: stdout on success, (stdout, stderr, retcode) if requested, or None on critical error/unignored failure.
    """
    try:
        cmd_list = ['git'] + [str(c) for c in command] # Prepend 'git' and ensure all parts are strings
        perform_check = not ignore_errors and not expect_to_fail # Raise error on failure only if not ignoring/expecting failure

        process = subprocess.run(
            cmd_list, capture_output=True, text=True, encoding='utf-8', 
            errors='replace', # Handle potential decoding errors in git output
            cwd=working_dir, check=perform_check, shell=False # Use safe execution, no shell            
        )
        # Return tuple if explicitly requested
        if ignore_errors or expect_to_fail:
            return process.stdout.strip(), process.stderr.strip(), process.returncode
        # Otherwise, return only stdout on success
        return process.stdout.strip()
    except subprocess.CalledProcessError as e: # Command returned non-zero exit code
        # Only print error if we weren't expecting/ignoring it
        if not ignore_errors and not expect_to_fail: 
            # Avoid excessive noise from expected failures (like git show non-existent file)
            # print(f"{Colors.RED}Git command failed: {' '.join(map(str, e.cmd))}{Colors.RESET}")
            # print(f"{Colors.RED}Stderr: {e.stderr.strip() if e.stderr else 'No stderr output'}{Colors.RESET}")
            pass # Suppress output here, caller handles None return
        # Return None or tuple based on flags
        if ignore_errors or expect_to_fail:
             return e.stdout.strip(), e.stderr.strip(), e.returncode
        else:
             return None 
    except FileNotFoundError: # 'git' command itself was not found
        print(f"{Colors.RED}Error: Git command not found. Is Git installed and in your PATH?{Colors.RESET}")
        if ignore_errors or expect_to_fail: 
            return ("", "Git command not found.", 127) # Simulate failure tuple
        return None # Critical error

