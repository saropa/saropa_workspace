def analyze_original_json_diff(diff_text):
    """
    Performs the original, simpler heuristic analysis for JSON files.
    Looks for added/removed lines starting with '"key":' to suggest key changes.
    Implements part of F11.
    Args:
        diff_text (str): Textual Git diff.
    Returns:
        list: A list of strings describing added/removed JSON keys.
    """
    analysis_summary = [] # F11 JSON analysis
    if not diff_text: return analysis_summary
    added_keys, removed_keys = set(), set() # Sets to store unique added/removed keys

    # Extract added lines (excluding diff header) and strip leading '+' and whitespace
    added_lines = [line[1:].strip() for line in diff_text.splitlines() if line.startswith('+') and not line.startswith('+++')]
    # Extract removed lines (excluding diff header) and strip leading '-' and whitespace
    removed_lines = [line[1:].strip() for line in diff_text.splitlines() if line.startswith('-') and not line.startswith('---')]

    # Extract potential keys from added lines
    for line_content in added_lines:
        match = re.match(r'"([\w\d_-]+)"\s*:', line_content) # Match key pattern: "key": value
        if match: # If a match was found
            added_keys.add(match.group(1)) # Add the captured key (group 1) to the set

    # Extract potential keys from removed lines
    for line_content in removed_lines:
        match = re.match(r'"([\w\d_-]+)"\s*:', line_content) # Match key pattern
        if match: # If a match was found
            removed_keys.add(match.group(1)) # Add the captured key to the set

    # Format findings into the summary list
    if added_keys: analysis_summary.append(f"Added JSON keys: {', '.join(sorted(list(added_keys)))}")
    if removed_keys: analysis_summary.append(f"Removed JSON keys: {', '.join(sorted(list(removed_keys)))}")

    # Add generic notes if changes detected but no specific key patterns matched
    if not analysis_summary and (added_lines or removed_lines):
        analysis_summary.append("General content changes detected in JSON (no specific key patterns found).")
    elif not (added_lines or removed_lines) and diff_text: # Diff exists but no lines added/removed
         analysis_summary.append("Textual changes present, but no specific key patterns matched for this analysis.") # Less likely case

    return analysis_summary

def analyze_dependency_changes(diff_text, old_content_str, new_content_str, is_lock_file=False, file_status_for_content=None):
    """
    Analyzes changes between old and new versions of pubspec.yaml or pubspec.lock content.
    Parses YAML to identify added, removed, and version-changed dependencies.
    Implements F16.
    Args:
        diff_text (str): Git diff output (used for context in notes).
        old_content_str (str or None): File content before the change.
        new_content_str (str or None): File content after the change.
        is_lock_file (bool): True if analyzing pubspec.lock, False for pubspec.yaml.
        file_status_for_content (str, optional): Status char ('A', 'D', 'M', 'C', 'R') of the file for content fetching context.
    Returns:
        dict or None: Dictionary containing 'added', 'removed', 'version_changed' lists (F16 structure), or None if no changes.
    """
    changes = {"added": [], "removed": [], "version_changed": [], "notes": None}; file_type = "pubspec.lock" if is_lock_file else "pubspec.yaml"
    
    # Determine if analysis is possible based on content availability and type
    if (old_content_str is None and not (file_status_for_content == 'A' or file_status_for_content == 'C')) or \
       (new_content_str is None and not (file_status_for_content == 'D')):
        changes["notes"] = f"Content for {file_type} unavailable/binary; cannot analyze dependencies."
        return changes

    effective_old_content = old_content_str if old_content_str is not None else ""
    effective_new_content = new_content_str if new_content_str is not None else ""
    
    # Safely parse the YAML content, handling potential errors or empty content
    try: 
        old_data = yaml.safe_load(effective_old_content) if effective_old_content else {}; new_data = yaml.safe_load(effective_new_content) if effective_new_content else {}
        # Ensure loaded data are dictionaries for consistent access later
        old_data = old_data if isinstance(old_data, dict) else {}; new_data = new_data if isinstance(new_data, dict) else {}
    except yaml.YAMLError as e: changes["notes"] = f"Error parsing {file_type} content: {e}"; return changes
    
    old_deps_map, new_deps_map = {}, {} # Dictionaries to hold name: version/spec pairs
    
    if is_lock_file: # F16: Logic specific to pubspec.lock structure
        old_pkgs=old_data.get('packages',{})or{}; new_pkgs=new_data.get('packages',{})or{}
        # Extract name: version pairs from the 'packages' section
        for n,d in (old_pkgs.items()if isinstance(old_pkgs,dict)else[]): old_deps_map[n]=d.get('version') if isinstance(d,dict) else str(d) if isinstance(d,str)else None
        for n,d in (new_pkgs.items()if isinstance(new_pkgs,dict)else[]): new_deps_map[n]=d.get('version') if isinstance(d,dict) else str(d) if isinstance(d,str)else None
    else: # F16: Logic specific to pubspec.yaml structure
        # Combine 'dependencies', 'dev_dependencies', and 'dependency_overrides' sections
        old_deps_map = {**(old_data.get('dependencies', {}) or {}), **(old_data.get('dev_dependencies', {}) or {}), **(old_data.get('dependency_overrides', {}) or {})}; 
        new_deps_map = {**(new_data.get('dependencies', {}) or {}), **(new_data.get('dev_dependencies', {}) or {}), **(new_data.get('dependency_overrides', {}) or {})}
    
    # F16: Use set operations to find added and removed dependency names
    changes["added"] = sorted(list(set(new_deps_map.keys()) - set(old_deps_map.keys())))
    changes["removed"] = sorted(list(set(old_deps_map.keys()) - set(new_deps_map.keys())))
    
    # F16: Check for version changes in dependencies present in both old and new maps
    for n in set(old_deps_map.keys()) & set(new_deps_map.keys()):
        oS, nS = old_deps_map.get(n), new_deps_map.get(n)
        # Compare string representations to handle complex version specs (e.g., git paths) simply
        if str(oS) != str(nS): 
            changes["version_changed"].append({"name": n, "from": str(oS) if oS is not None else "N/A", "to": str(nS) if nS is not None else "N/A"})
    
    # Determine if any changes were actually detected
    has_dc = changes["added"] or changes["removed"] or changes["version_changed"]
    
    # Add notes if appropriate
    if not has_dc and not changes.get("notes") and diff_text: changes["notes"] = f"{file_type} content changed, but no specific dependency alterations were detected."
    # Remove redundant "no change" notes if actual changes *were* detected (and note isn't an error)
    if has_dc and changes.get("notes") and "Error parsing" not in changes["notes"] and "unavailable/binary" not in changes["notes"]: changes.pop("notes", None) 
    
    # Return None if no actual changes occurred and there are no error notes
    if not has_dc and not changes.get("notes"): return None
    
    return changes

def analyze_arb_file_changes(old_content_str, new_content_str, file_status_for_content=None):
    """
    Analyzes .arb (JSON) localization files for changes in translatable keys (those not starting with '@').
    Implements F18.
    Args:
        old_content_str (str or None): ARB file content before change.
        new_content_str (str or None): ARB file content after change.
        file_status_for_content (str, optional): Status char ('A', 'D', 'M', 'C', 'R') of the file for content fetching context.
    Returns:
        dict or None: Dictionary containing 'keys_added', 'keys_removed' (F18 structure), or None if no key changes.
    """
    changes = {"keys_added": [], "keys_removed": [], "notes": None}

    if (old_content_str is None and not (file_status_for_content == 'A' or file_status_for_content == 'C')) or \
       (new_content_str is None and not (file_status_for_content == 'D')):
        changes["notes"] = "Content for .arb file unavailable/binary; cannot analyze keys."
        return changes
        
    effective_old_content = old_content_str if old_content_str is not None else ""
    effective_new_content = new_content_str if new_content_str is not None else ""
    
