    try: 
        old_data = json.loads(effective_old_content) if effective_old_content and effective_old_content.strip() else {}
        new_data = json.loads(effective_new_content) if effective_new_content and effective_new_content.strip() else {}
    except json.JSONDecodeError as e: changes["notes"] = f"Error parsing ARB JSON content: {e}"; return changes
    
