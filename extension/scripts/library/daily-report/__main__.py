    old_keys = {k for k in old_data if not k.startswith('@')}
    new_keys = {k for k in new_data if not k.startswith('@')}
    
    # F18: Use set operations to find added and removed keys
    changes["keys_added"] = sorted(list(new_keys - old_keys))
    changes["keys_removed"] = sorted(list(old_keys - new_keys))
    
    has_kc = changes["keys_added"] or changes["keys_removed"]
    
    # Add notes if appropriate
    if not has_kc and not changes.get("notes") and (old_content_str or "") != (new_content_str or ""): changes["notes"] = "Localization file content changed, but no translatable keys were added or removed."
    if has_kc and changes.get("notes") and "Error parsing" not in changes["notes"] and "unavailable/binary" not in changes["notes"]: changes.pop("notes", None) 
    
