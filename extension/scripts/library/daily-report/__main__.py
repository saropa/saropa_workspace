def get_file_content_at_revision(file_path, revision="HEAD"):
    """
    Retrieves the content of a file at a specific Git revision using `git show`.
    Returns None if the file is likely binary or if an error occurs.
    Utilizes FILE_CONTENT_CACHE.
    """
    cache_key = (file_path, str(revision))
    if cache_key in FILE_CONTENT_CACHE:
        return FILE_CONTENT_CACHE[cache_key]

    content, stderr_show, retcode_show = run_git_command(['show', f'{revision}:{file_path}'], ignore_errors=True)

    result = None # Default to None
    if retcode_show == 0 and content is not None:
        # Heuristic 1: Git's own indication in 'show' output (less common for raw content)
        if "Binary Sfile" in content or \
           ("binary files" in content.lower() and "differ" in content.lower()): # Check for "binary files ... differ"
            result = None # Treat as binary
        else:
            # Heuristic 2: Check for a high proportion of null bytes,
            # but only if the content is reasonably sized to avoid misclassifying small text files
            # with a few accidental nulls, or huge text files where a few nulls are insignificant.
            # This is a common characteristic of many binary formats.
            # Small files are less likely to be binary based *solely* on a few nulls if git doesn't say so.
            # Large files with very few nulls are likely text.
            try:
                # Attempt to decode as UTF-8 to check for actual text-ness.
                # If this succeeds without error and without too many replacement chars, it's likely text.
                decoded_content = content.encode('utf-8', 'surrogatepass').decode('utf-8', 'replace')
                num_replacements = decoded_content.count('\uFFFD') # Unicode replacement character

                # If it has many null bytes (raw content) OR many replacement characters after decode
                if (len(content) > 64 and content.count('\0') > (len(content) // 20)) or \
                   (len(decoded_content) > 64 and num_replacements > (len(decoded_content) // 20)):
                    result = None # Likely binary
                else:
                    result = content # Likely text
            except UnicodeDecodeError:
                result = None # If it can't even be decoded with replacements, it's almost certainly binary
            except Exception: # Catch any other unexpected errors during this check
                result = None 
    # If retcode_show is not 0, 'git show' failed (e.g. file not found at revision), so result remains None

    FILE_CONTENT_CACHE[cache_key] = result
    return result
